package com.avereon.cartesia.math;

public class CadMathExpressionException extends Exception {

	public CadMathExpressionException() {
		super();
	}

	public CadMathExpressionException( String message ) {
		super( message );
	}

	public CadMathExpressionException( String message, Throwable cause ) {
		super( message, cause );
	}

	public CadMathExpressionException( Throwable cause ) {
		super( cause );
	}

}
